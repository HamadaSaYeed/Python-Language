#----------------------------------- >> Type Method << -----------------------------------#

x = 3

y = 'Hello World'

z = 8.4

w = True

list = ['c++','python']

set = {'Css','Html','js'}

tuple = ('Egypt','English')


print(type(x)) # <class 'int'>

print(type(y)) # <class 'str'>

print(type(z)) # <class 'float'>

print(type(w)) # <class 'bool'>

print(type(list)) # <class 'list'>

print(type(set)) # <class 'set'>

print(type(tuple)) # <class 'tuple'>